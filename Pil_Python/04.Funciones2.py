#-----------------FUNCIONES CON PARAMETROS O ARGUMENTOS----------------#
#Declaracion de una Funcion sin parametros
def suma():
	num1 = 5;
	num2 = 7;
	print(num1 + num2);
#llamamos la funcion
suma();
#La Principal Utilidad de una Funcion es la Reutilizacion de Codigo
suma();

#Declaracion De una Funcion con Parametros
#Una funcion en python puede recibir uno o mas parametros
def sumaP(num1, num2):
	print(num1 + num2);
	#fin F
#Para Llamar una Funcion con Parametros hay que incluir los Parametros
#o los valores pedidos en los parametros
sumaP(5, 4);
sumaP(10, 50);
sumaP(12502154, 2547852);
