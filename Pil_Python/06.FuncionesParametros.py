# Declaracion de una funcion con parametros
# Los paramtros o argumentos iran dentro del parentesis de la funcion
def sumador(num1, num2):
	print(num1 + num2);
# LLamado de la funcion con parametros
# las funciones que tienen parametros, es obligatorio incluir los parametros definifos en la funcion
sumador(4, 10);	
sumador(20, 100);	