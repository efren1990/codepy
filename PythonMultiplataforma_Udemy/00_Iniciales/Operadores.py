"""
RELACIONALES
Son utilizados para verificar condiciones y juzgar si son verdaderas o falsas

*Igualdad: == -> hace una comparacion de igualda entre dos datos
    (x == Y) x es igual a y
*Diferencia: != -> valida si dos datos son diferentes
    (x != y) x es diferente de y
"""
#Validacion con Igualdad en condicional if
if (100 == 100):
    print("Son iguales")
else:
    print("No son iguales")
#Validacion con Diferencia en Condicional if
if(100 != 50):
    print("Son diferentes")
else:
    print("No son diferentes")
